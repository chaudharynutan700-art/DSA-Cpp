#include<bits/stdc++.h>
using namespace std;
int main(){
    string s;
    cin>>s;
    int count=0;
    for(int i=0; i<s.length() ;i++){
        if(s[i]=='4' or s[i]=='7'){
            count++;
          }}
    if(count==4  or count==7){
        cout<<"YES";
    }
    else{
        cout<<"NO";}
     return 0;
}